@router.post(
    "/{scan_id}/cancel",
    response_model=ScanCancelResponse,
    status_code=status.HTTP_202_ACCEPTED,
    summary="Request scan cancellation",
    description=(
        "Sets a cancellation flag in Redis.  The scan orchestrator checks this "
        "flag between pipeline stages and transitions the scan to 'cancelled'. "
        "Returns 202 immediately — the scan may still be running for a short "
        "time while the current stage completes."
    ),
    operation_id="cancel_scan",
    responses={
        202: {"description": "Cancellation requested."},
        404: {"model": ErrorResponse, "description": "Scan not found."},
        409: {"model": ErrorResponse, "description": "Scan is already completed or failed."},
    },
)
async def cancel_scan(
    scan_id: uuid.UUID,
    scan_repo: ScanRepository = Depends(get_scan_repository),
    redis: RedisClient = Depends(get_redis_client),
) -> ScanCancelResponse:
    """Request cancellation for a queued or running scan."""
    logger.info("api.cancel_scan", scan_id=str(scan_id))

    scan = await scan_repo.get_scan(scan_id)
    terminal = {ScanStatus.COMPLETED.value, ScanStatus.FAILED.value, ScanStatus.CANCELLED.value}
    if scan.status in terminal:
        from app.core.exceptions import AppError
        raise AppError(
            f"Scan '{scan_id}' is already in terminal state '{scan.status}'.",
            status_code=409,
        )

    await redis.request_cancellation(scan_id)
    logger.info("api.cancel_scan.flag_set", scan_id=str(scan_id))

    return ScanCancelResponse(scan_id=scan_id, status="cancellation_requested")


