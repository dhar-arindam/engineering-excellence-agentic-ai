_AGENT_CATEGORY: dict[str, str] = {
    "qa": "qa",
    "developer": "dev",
    "senior_developer": "dev",
    "architect": "arch",
    "senior_architect": "arch",
    "sre": "sre",
    "senior_sre": "sre",
    "security": "security",
    "security_expert": "security",
}
_VALID_SEVERITIES = {"low", "medium", "high", "critical"}


@router.get(
    "/{scan_id}/patch-annotations",
    response_model=list[AnnotationResponse],
    summary="Get inline diff annotations",
    description=(
        "Returns code-level findings that have a ``file_path`` and ``line_number``, "
        "making them suitable for inline rendering in a diff or patch viewer.  "
        "Optionally filter by ``severity`` or ``category``."
    ),
    operation_id="get_scan_patch_annotations",
    responses={
        200: {"description": "List of inline annotations."},
        404: {"model": ErrorResponse, "description": "Scan not found."},
    },
)
async def get_scan_patch_annotations(
    scan_id: uuid.UUID,
    severity: str | None = Query(default=None, description="Filter: low | medium | high | critical."),
    category: str | None = Query(default=None, description="Filter: qa | dev | arch | sre | security."),
    limit: int = Query(default=200, ge=1, le=1000),
    offset: int = Query(default=0, ge=0),
    scan_repo: ScanRepository = Depends(get_scan_repository),
) -> list[AnnotationResponse]:
    scan = await scan_repo.get_scan(scan_id)
    annotations: list[AnnotationResponse] = []

    for agent_result in scan.agent_results or []:
        issues = agent_result.issues if isinstance(agent_result.issues, list) else []
        cat = _AGENT_CATEGORY.get(agent_result.agent_name.lower(), "dev")
        for issue in issues:
            if not isinstance(issue, dict):
                continue
            sev = str(issue.get("severity", "medium")).lower()
            if sev not in _VALID_SEVERITIES:
                sev = "medium"
            file_path = issue.get("file_path") or issue.get("location") or None
            line_raw = issue.get("line_number") or issue.get("line")
            line_number: int | None = None
            try:
                line_number = int(line_raw) if line_raw is not None else None  # type: ignore[arg-type]
            except (TypeError, ValueError):
                pass
            # patch-annotations only includes findings with location info
            if not file_path and not line_number:
                continue
            annotations.append(AnnotationResponse(
                file_path=file_path,
                line_number=line_number,
                severity=sev,
                category=cat,
                message=str(issue.get("description") or issue.get("message") or ""),
            ))

    if severity:
        annotations = [a for a in annotations if a.severity == severity.lower()]
    if category:
        annotations = [a for a in annotations if a.category == category.lower()]

    return annotations[offset : offset + limit]


