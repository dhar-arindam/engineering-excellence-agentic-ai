# Helpers for /upload
# ---------------------------------------------------------------------------


def _is_skipped_path(rel_path: str) -> bool:
    """Return True if the relative file path should be excluded from uploads."""
    parts = rel_path.replace("\\", "/").split("/")
    return any(part in _SKIP_DIR_NAMES for part in parts)


def _safe_write(base: Path, rel_path: str, data: bytes) -> None:
    """Write *data* to *base/rel_path*, blocking any traversal attempt."""
    rel_path = rel_path.replace("\\", "/").lstrip("/")
    for segment in rel_path.split("/"):
        if segment in ("", ".", ".."):
            raise ValidationError(f"Unsafe path in upload: {rel_path!r}")
    dest = (base / rel_path).resolve()
    if not str(dest).startswith(str(base.resolve())):
        raise ValidationError(f"Path traversal detected: {rel_path!r}")
    dest.parent.mkdir(parents=True, exist_ok=True)
    dest.write_bytes(data)


def _assert_has_source_files(directory: Path) -> None:
    """Raise ValidationError if the directory has no recognised source files."""
    for _, _, filenames in os.walk(str(directory)):
        for fname in filenames:
            if Path(fname).suffix.lower() in _SOURCE_EXTENSIONS:
                return
    raise ValidationError(
        "No recognised source files found in the uploaded folder. "
        "Expected files such as .py, .js, .ts, .java, .go, .rb, .php, .cs, .cpp, .rs."
    )


def _apply_gitignore_filters(directory: Path) -> int:
    """Parse every .gitignore found under *directory* and delete matching files.

    Each ``.gitignore`` applies to files in the directory that contains it and
    all of its subdirectories (same semantics as Git).  The function walks the
    tree in top-down order so parent ignore rules are processed before children.

    Returns the number of files deleted.
    """
    deleted = 0
    # Collect (gitignore_dir, pathspec) pairs, shallowest first.
    matchers: list[tuple[Path, pathspec.PathSpec]] = []

    for root, dirs, files in os.walk(str(directory), topdown=True):
        # Skip already-excluded dirs so we don't look inside them.
        dirs[:] = [d for d in dirs if d not in _SKIP_DIR_NAMES]

        if ".gitignore" not in files:
            continue
        gi_path = Path(root) / ".gitignore"
        try:
            lines = gi_path.read_text(encoding="utf-8", errors="replace").splitlines()
        except OSError:
            continue
        spec = pathspec.PathSpec.from_lines("gitwildmatch", lines)
        matchers.append((Path(root), spec))

    if not matchers:
        return 0

    # Second pass: delete files that match any applicable gitignore spec.
    for root, _, files in os.walk(str(directory), topdown=True):
        for fname in files:
            if fname == ".gitignore":
                continue  # keep the gitignore file itself
            file_path = Path(root) / fname
            for gi_dir, spec in matchers:
                # Only apply this spec to files inside its directory.
                try:
                    rel = file_path.relative_to(gi_dir)
                except ValueError:
                    continue  # file is not under this gitignore's directory
                if spec.match_file(str(rel).replace("\\", "/")):
                    try:
                        file_path.unlink()
                        deleted += 1
                    except OSError:
                        pass
                    break  # no need to check further matchers for this file

    # Remove directories that are now empty (best-effort).
    for root, dirs, files in os.walk(str(directory), topdown=False):
        if root == str(directory):
            continue
        try:
            if not any(Path(root).iterdir()):
                Path(root).rmdir()
        except OSError:
            pass

    return deleted


