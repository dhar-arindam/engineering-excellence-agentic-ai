def _resolve_source_reference(request: ScanRunRequest) -> tuple[str, str]:
    """Return (source_reference, repo_name) for the given request.

    Note: GitHub web tree URLs are already normalised by ``ScanRunRequest._validate_source``
    before this function is called, so ``repository_url`` is always a clean clone URL here.
    """
    if request.source_type == "github":
        url = request.repository_url or ""
        # removesuffix (Python 3.9+) — rstrip(".git") is a character-strip bug
        name = url.rstrip("/").removesuffix(".git").rsplit("/", 1)[-1] or "repository"
        return url, name

    if request.source_type == "local":
        path = request.local_path or ""
        name = Path(path).name or "repository"
        return path, name

    raise ValidationError(f"Unsupported source_type: '{request.source_type}'.")


def _scan_to_summary(scan: ScanModel) -> ScanSummarySchema:
    """Map a ScanModel to the frontend-aligned ScanSummarySchema (delegates to mapper)."""
    repo_name = ""
    if hasattr(scan, "repository") and scan.repository is not None:
        repo_name = scan.repository.name or ""
    return scan_to_summary(scan, repo_name)



