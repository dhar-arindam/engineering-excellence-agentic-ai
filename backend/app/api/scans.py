@router.post(
    "/run",
    response_model=ScanRunResponse,
    status_code=status.HTTP_202_ACCEPTED,
    summary="Trigger a new scan",
    description=(
        "Validates the source, acquires a per-repository distributed lock, "
        "creates a scan record (status='queued'), enqueues the scan on the "
        "Arq task queue, and immediately returns the scan and repository IDs. "
        "Returns 409 if a scan is already running for the same repository."
    ),
    operation_id="run_scan",
    responses={
        202: {
            "description": "Scan queued; scan_id and repository_id returned.",
            "content": {
                "application/json": {
                    "example": {
                        "scan_id": "3fa85f64-5717-4562-b3fc-2c963f66afa6",
                        "repository_id": "9b1deb4d-3b7d-4bad-9bdd-2b0d7b3dcb6d",
                        "status": "queued",
                    }
                }
            },
        },
        400: {"model": ErrorResponse, "description": "Invalid source input."},
        409: {"model": ErrorResponse, "description": "Scan already running for this repository."},
        422: {"model": ErrorResponse, "description": "Pydantic validation error."},
        500: {"model": ErrorResponse, "description": "Unexpected server error."},
    },
)
async def run_scan(
    http_request: Request,
    request: ScanRunRequest,
    scan_repo: ScanRepository = Depends(get_scan_repository),
    redis: RedisClient = Depends(get_redis_client),
) -> ScanRunResponse:
    """Queue a new scan and return immediately.

    The heavy lifting (cloning, agent execution, scoring) runs in the Arq
    worker process.  Poll ``GET /api/scans/{scan_id}/status`` for progress.
    """
    logger.info(
        "api.run_scan.start",
        source_type=request.source_type,
        repository_url=request.repository_url,
        local_path=request.local_path,
        branch=request.branch,
        scan_mode=(request.config.mode if request.config else "deep"),
    )

    source_reference, repo_name = _resolve_source_reference(request)

    # Pre-flight local path validation — returns a 400 immediately so the
    # client doesn't have to wait for the background worker to report failure.
    if request.source_type == "local":
        validator = LocalRepoValidator()
        source_reference = await validator.validate(source_reference)
        repo_name = Path(source_reference).name

    # Find or create the repository record.
    repository = await scan_repo.find_or_create_repository(
        name=repo_name,
        repo_url=request.repository_url,
        source_type=request.source_type,
    )

    # Acquire the per-repository distributed lock (TTL = scan_lock_ttl_seconds).
    # We need a temporary scan_id to store in the lock value so the worker
    # can verify ownership when releasing.
    import uuid as _uuid
    from app.core.config import settings

    tentative_scan_id = _uuid.uuid4()
    acquired = await redis.acquire_repo_lock(
        repository_id=repository.id,
        scan_id=tentative_scan_id,
        ttl_seconds=settings.scan_lock_ttl_seconds,
    )
    if not acquired:
        raise ScanAlreadyRunningError(str(repository.id))

    # Create the scan record with status="queued", using the same UUID.
    config_dict = request.config.model_dump() if request.config else None
    scan_mode = (request.config.mode if request.config else None) or "deep"
    scan = await scan_repo.create_scan(
        repository_id=repository.id,
        source_type=request.source_type,
        source_reference=source_reference,
        scan_id=tentative_scan_id,
        branch=request.branch,
        scan_mode=scan_mode,
        scan_config_json=config_dict,
    )

    # Enqueue on the Arq task queue.
    job_id = await enqueue_scan(
        pool=http_request.app.state.arq_pool,
        scan_id=scan.id,
        repository_id=repository.id,
    )

    logger.info(
        "api.run_scan.queued",
        scan_id=str(scan.id),
        repository_id=str(repository.id),
        job_id=job_id,
    )
    return ScanRunResponse(
        scan_id=scan.id,
        repository_id=repository.id,
        status=ScanStatus.QUEUED.value,
    )


