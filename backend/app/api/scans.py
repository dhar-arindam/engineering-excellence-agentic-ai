@router.post(
    "/upload",
    response_model=ScanRunResponse,
    status_code=status.HTTP_202_ACCEPTED,
    summary="Upload a local repository folder and scan it",
    description=(
        "Accept a folder upload (browser webkitdirectory picker) or a single "
        ".zip archive, extract to a shared temp volume, and queue a scan.\n\n"
        "Works from **any OS** — Windows, Mac, Linux — whether the backend runs "
        "locally, in Docker, or in Azure. No volume mounts or path configuration required."
    ),
    operation_id="upload_scan",
    responses={
        202: {"description": "Scan queued; scan_id and repository_id returned."},
        400: {"model": ErrorResponse, "description": "Invalid upload or no source files found."},
        409: {"model": ErrorResponse, "description": "Scan already running for this repository."},
        413: {"model": ErrorResponse, "description": "Upload exceeds size limit."},
        422: {"model": ErrorResponse, "description": "Validation error."},
    },
)
async def upload_scan(
    http_request: Request,
    files: list[UploadFile] = File(
        ...,
        description=(
            "Project files from a folder picker (webkitdirectory) or a single .zip archive. "
            "node_modules, .git, and build artifacts should be excluded by the client."
        ),
    ),
    repo_name: str = Form(..., description="Human-readable name for this project."),
    config: Optional[str] = Form(default=None, description="JSON-serialised ScanConfig."),
    scan_repo: ScanRepository = Depends(get_scan_repository),
    redis: RedisClient = Depends(get_redis_client),
) -> ScanRunResponse:
    """Queue a scan from an uploaded folder or ZIP archive.

    Files are written to the shared ``/tmp/scans/{scan_id}/`` volume that is
    mounted in both the API container and every Arq worker container.
    """
    import uuid as _uuid
    from app.core.config import settings

    scan_id = _uuid.uuid4()
    upload_dir = _SCAN_TMP_DIR / str(scan_id)
    upload_dir.mkdir(parents=True, exist_ok=True)

    try:
        total_bytes = 0

        is_zip = (
            len(files) == 1
            and (files[0].filename or "").lower().endswith(".zip")
        )

        if is_zip:
            # ── ZIP upload ────────────────────────────────────────────────
            raw = await files[0].read()
            if len(raw) > _UPLOAD_MAX_TOTAL_BYTES:
                raise AppError(
                    f"Upload exceeds the {_UPLOAD_MAX_TOTAL_BYTES // 1_048_576} MB limit.",
                    status_code=413,
                )
            try:
                with zipfile.ZipFile(io.BytesIO(raw)) as zf:
                    for info in zf.infolist():
                        if info.is_dir():
                            continue
                        rel = info.filename.replace("\\", "/")
                        if _is_skipped_path(rel):
                            continue
                        if info.file_size > _UPLOAD_MAX_FILE_BYTES:
                            continue
                        data = zf.read(info.filename)
                        _safe_write(upload_dir, rel, data)
            except zipfile.BadZipFile as exc:
                raise ValidationError("Uploaded file is not a valid ZIP archive.") from exc

        else:
            # ── Multi-file folder upload ───────────────────────────────────
            for uf in files:
                rel_path = uf.filename or "unknown"
                if _is_skipped_path(rel_path):
                    await uf.read()  # drain to avoid resource leak
                    continue
                data = await uf.read()
                if len(data) > _UPLOAD_MAX_FILE_BYTES:
                    continue  # silently skip oversized individual files
                total_bytes += len(data)
                if total_bytes > _UPLOAD_MAX_TOTAL_BYTES:
                    raise AppError(
                        f"Upload exceeds the {_UPLOAD_MAX_TOTAL_BYTES // 1_048_576} MB limit.",
                        status_code=413,
                    )
                _safe_write(upload_dir, rel_path, data)

        # ── Apply .gitignore rules (safety net — also covers ZIP uploads) ─
        _removed = _apply_gitignore_filters(upload_dir)
        if _removed:
            logger.info("upload.gitignore_filtered", files_removed=_removed)

        _assert_has_source_files(upload_dir)

        # ── Parse optional ScanConfig ────────────────────────────────────
        config_dict: dict | None = None
        scan_mode = "deep"
        if config:
            try:
                config_dict = json.loads(config)
                scan_mode = config_dict.get("mode", "deep")
            except json.JSONDecodeError:
                pass

        # ── Find-or-create repository record ────────────────────────────
        repository = await scan_repo.find_or_create_repository(
            name=repo_name.strip() or "uploaded-repo",
            repo_url=None,
            source_type="local",
        )

        # ── Per-repo distributed lock ────────────────────────────────────
        acquired = await redis.acquire_repo_lock(
            repository_id=repository.id,
            scan_id=scan_id,
            ttl_seconds=settings.scan_lock_ttl_seconds,
        )
        if not acquired:
            raise ScanAlreadyRunningError(str(repository.id))

        # ── Create scan record ────────────────────────────────────────────
        scan = await scan_repo.create_scan(
            repository_id=repository.id,
            source_type="local",
            source_reference=str(upload_dir),
            scan_id=scan_id,
            scan_mode=scan_mode,
            scan_config_json=config_dict,
        )

        # ── Enqueue on Arq ────────────────────────────────────────────────
        job_id = await enqueue_scan(
            pool=http_request.app.state.arq_pool,
            scan_id=scan.id,
            repository_id=repository.id,
        )

        logger.info(
            "api.upload_scan.queued",
            scan_id=str(scan.id),
            repo_name=repo_name,
            file_count=len(files),
            total_bytes=total_bytes,
            job_id=job_id,
        )
        return ScanRunResponse(
            scan_id=scan.id,
            repository_id=repository.id,
            status=ScanStatus.QUEUED.value,
        )

    except (AppError, ValidationError):
        shutil.rmtree(upload_dir, ignore_errors=True)
        raise
    except Exception:
        shutil.rmtree(upload_dir, ignore_errors=True)
        raise


