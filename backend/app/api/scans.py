"""Scan API routes.

Endpoints
---------
POST  /api/scans/run                       — validate, lock repo, create scan record,
                                             enqueue Arq job, return {scan_id, repository_id, status}.
GET   /api/scans/{scan_id}                 — full scan detail with agent findings.
GET   /api/scans/{scan_id}/status          — return live progress and status from DB.
POST  /api/scans/{scan_id}/cancel          — request scan cancellation via Redis flag.
GET   /api/scans/{scan_id}/patch           — unified diff generated by the auto-fix pipeline.
POST  /api/scans/{scan_id}/create-pr       — create GitHub PR from the stored patch.
GET   /api/scans/{scan_id}/patch-annotations — inline annotations keyed to the diff.

Per-repository lock
-------------------
Before enqueueing, the handler acquires a distributed Redis lock keyed by
``repository_id``.  If the lock is already held (another scan in progress),
a 409 is returned immediately.  The lock is released by the Arq worker after
the scan finishes (success, failure, or cancellation).

Cancellation protocol
---------------------
The cancel endpoint sets a Redis flag ``scan:cancel:{scan_id}``.
The scan orchestrator polls this flag between pipeline stages and raises
``ScanCancelledError`` on detection, transitioning the scan to "cancelled".

Design rules
------------
- Zero business logic in handlers.
- Domain errors (ValidationError, NotFoundError, ScanAlreadyRunningError)
  are AppError subclasses → caught by the global handler in main.py.
- Unexpected exceptions propagate to FastAPI's default 500 handler.
"""
